const worldStatus = window.WORLD_STATUS_DATA ?? { acts: [], characters: [], summary: "" };
const sourceCharacters = new Map(worldStatus.characters.map((character) => [character.name, character]));
const sourceActs = new Map(worldStatus.acts.map((act) => [act.act_number, act]));

const characterFromSource = (id, name, role, introducedIn = 0, unlockClue = null) => {
  const source = sourceCharacters.get(name) ?? {};
  return {
    id,
    name,
    role,
    introducedIn,
    unlockClue,
    note: source.description ?? "尚未获得足够信息。",
    status: source.status ?? "",
  };
};

const sceneFromAct = (actNumber, playable) => {
  const act = sourceActs.get(actNumber) ?? {};
  const sourceScenes = act.scenes ?? [];
  const unique = (values) => [...new Set(values.filter(Boolean))];
  return {
    ...playable,
    actNumber,
    eyebrow: `第 ${actNumber} 幕 · ${act.title ?? playable.title}`,
    title: act.title ?? playable.title,
    description: act.dramatic_purpose ?? playable.description,
    briefing: {
      opening: act.opening_state ?? "",
      objective: unique(sourceScenes.map((scene) => scene.objective)).join(" / "),
      conflict: unique(sourceScenes.map((scene) => scene.conflict)).join(" "),
      locations: unique(sourceScenes.map((scene) => scene.location)),
      participants: unique(sourceScenes.flatMap((scene) => scene.participants ?? [])),
      nextHook: act.next_act_hook ?? "",
      sourceClueCount: act.clue_count ?? 0,
    },
  };
};

