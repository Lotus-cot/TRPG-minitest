import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const [, , worldStatusPath, actsPath] = process.argv;

if (!worldStatusPath || !actsPath) {
  console.error("Usage: node scripts/import-world-status.mjs <world-status.json> <world-status-acts.json>");
  process.exit(1);
}

const readJson = (filePath) => JSON.parse(fs.readFileSync(path.resolve(filePath), "utf8"));
const rawWorldStatus = readJson(worldStatusPath);
const rawActs = readJson(actsPath);
const worldState = rawWorldStatus.worldState ?? rawWorldStatus;

if (!Array.isArray(rawActs.acts) || !rawActs.acts.length) {
  throw new Error("The acts file must contain a non-empty acts array.");
}

const actNumbers = rawActs.acts.map((act) => act.act_number);
if (new Set(actNumbers).size !== actNumbers.length) {
  throw new Error("Every act must have a unique act_number.");
}

const playerSafeActs = rawActs.acts.map((act) => ({
  act_number: act.act_number,
  title: act.title,
  dramatic_purpose: act.dramatic_purpose,
  opening_state: act.opening_state,
  next_act_hook: act.next_act_hook,
  clue_count: act.clues_revealed?.length ?? 0,
  scenes: (act.scenes ?? []).map((scene) => ({
    title: scene.title,
    location: scene.location,
    time: scene.time,
    participants: scene.participants ?? [],
    objective: scene.objective,
    conflict: scene.conflict,
  })),
}));

const playerSafeCharacters = (worldState.characters ?? []).map((character) => ({
  name: character.name,
  description: character.description,
  status: character.status,
}));

const runtimeData = {
  schemaVersion: 1,
  importedAt: rawWorldStatus.savedAt ?? null,
  source: {
    savedAt: rawWorldStatus.savedAt ?? null,
    model: rawWorldStatus.model ?? null,
  },
  summary: rawActs.summary ?? worldState.summary ?? "",
  acts: playerSafeActs,
  characters: playerSafeCharacters,
};

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const outputPath = path.resolve(scriptDir, "..", "world-status-runtime.js");
const output = `// Generated by scripts/import-world-status.mjs. Re-run the importer instead of editing by hand.\nwindow.WORLD_STATUS_DATA = ${JSON.stringify(runtimeData, null, 2)};\n`;

fs.writeFileSync(outputPath, output, "utf8");
console.log(`Imported ${runtimeData.acts.length} acts and ${runtimeData.characters.length} characters into ${outputPath}`);
